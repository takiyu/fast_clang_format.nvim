import neovim

# -----------------------------------------------------------------------------
# --------------------------------- Constants ---------------------------------
# -----------------------------------------------------------------------------
DEFAULT_COMMENT_STR = '//'
DEFAULT_N_COL = 80
DEFAULT_SPLIT_CHAR = '-'
SPLIT_CHAR_CANDS = ['-', '=', '*', '#']


# -----------------------------------------------------------------------------
# ----------------------------------- Plugin ----------------------------------
# -----------------------------------------------------------------------------
@neovim.plugin
class FastClangFormatPlugin(object):

    def __init__(self, nvim):
        self.nvim = nvim

    @neovim.command('FastClangFormat', sync=True, nargs='?')
    def make_split_comment(self, args):
        self.nvim.current.buffer = ['this', 'is', 'a', 'pen']


